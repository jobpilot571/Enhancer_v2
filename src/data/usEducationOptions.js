/** Match extracted free text to a known option, else return Other + custom. */
export function matchOptionOrOther(value, options) {
  const raw = String(value || '').trim()
  if (!raw) return { select: '', custom: '' }
  const opts = options.map((o) => (typeof o === 'object' ? o.value : o))
  const exact = opts.find((o) => o.toLowerCase() === raw.toLowerCase())
  if (exact) return { select: exact, custom: '' }
  // Fuzzy contains
  const fuzzy = opts.find((o) => o !== 'Other' && (
    raw.toLowerCase().includes(o.toLowerCase())
    || o.toLowerCase().includes(raw.toLowerCase())
  ))
  if (fuzzy) return { select: fuzzy, custom: '' }
  // Degree aliases
  const aliases = {
    masters: "Master's",
    master: "Master's",
    ms: 'M.S.',
    'm.s': 'M.S.',
    mba: 'M.B.A.',
    bachelors: "Bachelor's",
    bachelor: "Bachelor's",
    bs: 'B.S.',
    'b.s': 'B.S.',
    ba: 'B.A.',
    'b.a': 'B.A.',
    phd: 'Ph.D.',
    'ph.d': 'Ph.D.',
  }
  const alias = aliases[raw.toLowerCase().replace(/\./g, '').replace(/'/g, '')]
    || aliases[raw.toLowerCase()]
  if (alias && opts.includes(alias)) return { select: alias, custom: '' }
  return { select: 'Other', custom: raw }
}
